namespace YAF.Web.TagHelpers;

/// <summary>
/// The body direction helper.
/// </summary>
[HtmlTargetElement("head")]
public class HeadTagHelper : TagHelper, IHaveServiceLocator
{
    /// <summary>
    ///   Gets or sets ServiceLocator.
    /// </summary>
    public IServiceLocator ServiceLocator => BoardContext.Current.ServiceLocator;

    /// <summary>
    /// The process.
    /// </summary>
    /// <param name="context">
    /// The context.
    /// </param>
    /// <param name="output">
    /// The output.
    /// </param>
    public override void Process(TagHelperContext context, TagHelperOutput output)
    {
        var items = BoardContext.Current.InlineElements.Items;

        // Inject Internal Stylesheet
        items.ForEach(
            script =>
                {
                    if (!script.IsInjected && script.Type == InlineType.Css)
                    {
                        output
                            .PostContent
                            .AppendHtml("<style>")
                            .AppendHtml(script.Code)
                            .AppendHtml("</style>");

                        script.IsInjected = true;
                    }
                });
    }
}